struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
}; 

typedef TreeNode node;
class Solution {
public:
    int goodNodes(node* root) {
        int max = 0;
        return _solve(root, max);
    }
private:
    int _solve(node* root, int max) {
        if (!root) return 0;

        int count = 0;

        if (root->val >= max) {
            ++count;
            max = root->val;
        }

        count+=_solve(root->left, max);
        count+=_solve(root->right, max);
        return count;
    }
};

