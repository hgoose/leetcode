class Solution {
public:
    int canCompleteCircuit(vector<int>& gas, vector<int>& cost) {
        if (std::accumulate(gas.begin(), gas.end(),0) < std::accumulate(cost.begin(), cost.end(),0)) return -1;
        int ret = 0;
        int tank = 0;
        for (int i=0; i<(int)gas.size(); ++i) {
            tank+=gas[i]-cost[i];
            if (tank < 0) {
                tank = 0;
                ret = i+1;
            }
        }
        return ret;
    }
};

